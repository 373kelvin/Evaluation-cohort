#!/usr/bin/env bash
# B3 — Run all tests and save output to outputs/b3-test-results/
set -euo pipefail

ROOT="$(cd "$(dirname "$0")/.." && pwd)"
OUT="$ROOT/outputs/b3-test-results"
mkdir -p "$OUT"
RESULTS="$OUT/RESULTS.md"
LOG="$OUT/test-run.log"

{
  echo "=== Evaluation 2.0 — Test Run ==="
  echo "Date: $(date -u '+%Y-%m-%d %H:%M:%S UTC')"
  echo ""

  echo "--- B4: fastapi_transactions (pytest) ---"
  echo "Framework: pytest"
  echo "Config: (defaults, no pytest.ini)"
  echo "Test file: fastapi_transactions/tests/test_main.py"
  echo "Command: cd fastapi_transactions && pytest tests/ -v"
  echo ""
  cd "$ROOT/fastapi_transactions"
  pip install -q -r requirements.txt 2>/dev/null || true
  python3 -m pytest tests/ -v || echo "[FAIL] fastapi_transactions tests failed"
  echo ""

  echo "--- A3: fraud-score-system/service (pytest) ---"
  echo "Framework: pytest"
  echo "Test file: fraud-score-system/service/tests/test_service.py"
  echo "Command: cd fraud-score-system/service && pytest -q"
  echo ""
  cd "$ROOT/fraud-score-system/service"
  pip install -q -r requirements.txt 2>/dev/null || true
  python3 -m pytest -q || echo "[FAIL] fraud-score-system service tests failed"
  echo ""

  echo "--- A3: fraud-score-system/engine (cargo test) ---"
  echo "Framework: cargo test (Rust)"
  echo "Test file: inline in engine/src/main.rs"
  echo "Command: cd fraud-score-system/engine && cargo test"
  echo ""
  cd "$ROOT/fraud-score-system/engine"
  if command -v cargo >/dev/null 2>&1; then
    cargo test 2>&1 || echo "[FAIL] Rust engine tests failed"
  else
    echo "[SKIP] cargo not installed — install Rust toolchain to run engine tests"
  fi
  echo ""

  echo "--- A3: integration test ---"
  echo "Test file: fraud-score-system/integration/test_integration.py"
  echo "Command: cd fraud-score-system && pytest -q integration/test_integration.py"
  echo ""
  cd "$ROOT/fraud-score-system"
  python3 -m pytest -q integration/test_integration.py 2>&1 || echo "[SKIP/FAIL] integration (needs cargo build --release)"
} 2>&1 | tee "$LOG"

# Write markdown summary
cat > "$RESULTS" << EOF
# B3 — Test Discovery and Execution Results

Generated by \`scripts/run_all_tests.sh\`.

## Test frameworks

| Project | Framework | Config | Test files |
|---------|-----------|--------|------------|
| fastapi_transactions | pytest | defaults | \`tests/test_main.py\` (8 tests) |
| fraud-score-system/service | pytest | \`conftest.py\` | \`service/tests/test_service.py\` (4 tests) |
| fraud-score-system/engine | cargo test | \`Cargo.toml\` | inline in \`engine/src/main.rs\` (4 tests) |
| fraud-score-system/integration | pytest | — | \`integration/test_integration.py\` |

## Commands

\`\`\`bash
cd fastapi_transactions && pytest tests/ -v
cd fraud-score-system/service && pytest -q
cd fraud-score-system/engine && cargo test
cd fraud-score-system && pytest -q integration/test_integration.py
\`\`\`

## Actual output

See \`test-run.log\` for full terminal output.

\`\`\`
$(tail -40 "$LOG")
\`\`\`

## Failures and interpretation

- **Integration test** skips if Rust binary is not built (\`cd engine && cargo build --release\`).
- **cargo test** skipped if Rust toolchain is not installed.
- All Python unit tests should pass without external services (in-memory stores).
EOF

echo ""
echo "[ok] Wrote $RESULTS and $LOG"
